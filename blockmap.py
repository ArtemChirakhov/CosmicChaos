class Map:
    def __init__(self, game):
        self.game = game
        self.block_map = block_map
        self.world_map = {}
        self.enemy_info = []
        self.get_map()
        self.current_lvl_id = 0
        self.difficulty = 0

    def get_map(self):
        self.enemy_info = []
        self.current_lvl_id = random.randint(0, len(block_map) - 1)
        for i in range(len(self.block_map[self.current_lvl_id])):
            for j in range(len(self.block_map[self.current_lvl_id][i])):
                if self.block_map[self.current_lvl_id][i][j] == 1:
                    # айди объектов мира:
                    # 0 - пустота
                    # 1 - стена
                    # 2 - враг
                    # можно сделать ещё сколько угодно
                    self.world_map[(j, i, 1)] = self.block_map[self.current_lvl_id][i][j]
                if self.block_map[self.current_lvl_id][i][j] == 2:
                    self.enemy_info.append((i, j))

    def draw(self):
        for pos in self.world_map:
            if pos[2] == 1:
                pygame.draw.rect(self.game.screen, 'darkgray', (pos[0] * 50, pos[1] * 50, 50, 50))

    def get_enemy_info(self):
        return self.enemy_info
